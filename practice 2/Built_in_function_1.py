from math import * # math library 안에 있는 모든 것을 사용
print(floor(5.99)) # 5 / # floor : 내림 , 소숫점 이하를 모두 삭제
print(ceil(3.14)) # 4 / # ceil : 올림 , 소숫점과 관계없이 정수자리 1 올림
print(sqrt(16)) # 4 / # sqrt : 제곱근 , 16의 제곱근을 구함

