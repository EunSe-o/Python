# \n : 줄바꿈 / 두 줄에 걸쳐 출력하고 싶을 때
print("백문이 불여일견\n백견이 불여일타")

# \", \' : " , ' 출력하고 싶을 때"
# '저는 "박은서"입니다.' 라고 출력
print("저는 \"박은서\"입니다.")
print("저는 \'박은서\'입니다.")

# \\ : 문장 내에서 \ 출력하고 싶을 때
print("c:\\Users\\박은서\\Desktop\\python")

# \r : 커서를 맨 앞으로 이동 (원래 글자 위에 덮어씀)
print("Red Apple\rPine")

# \b : 백스페이스 (한 글자 삭제)
print("Redd\bApple")

# \t : 탭
print("Red\tApple")